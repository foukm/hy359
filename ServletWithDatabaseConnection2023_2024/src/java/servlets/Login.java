    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        if(getCookieValue(request,"username")!=null && getCookieValue(request,"password")!=null){
            response.getWriter().write(getCookieValue(request,"username"));
        }
        else{
            response.getWriter().write("");
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String username = request.getParameter("username");
        String password = request.getParameter("password");
        String cookieValueUsername = getCookieValue(request, "username");
        String cookieValuePassword = getCookieValue(request, "password");
        
        if ((cookieValueUsername == null && cookieValuePassword==null) ||  !username.equals(cookieValueUsername) && !password.equals(cookieValuePassword) ) {
            response.addCookie(new Cookie("username", username));
            response.addCookie(new Cookie("password", password));
        }
    }
    
    public static String getCookieValue(HttpServletRequest request, String cookieName) {
        Cookie[] cookies = request.getCookies();
        if (cookies != null) {
            for (Cookie cookie : cookies) {
                if (cookieName.equals(cookie.getName())) {
                    return cookie.getValue();
                }
            }
        }
        return null;
    }

